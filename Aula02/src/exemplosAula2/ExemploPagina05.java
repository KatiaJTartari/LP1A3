package exemplosAula2;

public class ExemploPagina05 {

	public static void main(String[] args) {
		
		double a = 7, b = 3;
		double resultado;
		
		resultado = a + b;
		System.out.println(resultado);
		resultado = a - b;
		System.out.println(resultado);
		resultado = a * b;
		System.out.println(resultado);
		resultado = a / b;
		System.out.println(resultado);
		resultado = a % b;
		System.out.println(resultado);
		
		a++; // Equivale a: a = a + 1;
		System.out.println(a);
		a--; // Equivale a: a = a - 1;
		System.out.println(a);

	}

}
