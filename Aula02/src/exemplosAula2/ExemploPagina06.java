package exemplosAula2;

public class ExemploPagina06 {

	public static void main(String[] args) {
		
		double x = 8;
		
		x += 5; //Equivale a: x = + 5;
		System.out.println(x);
		x -= 3; //Equivale a: x = - 3;
		System.out.println(x);
		x *= 4; //Equivale a: x = * 4;
		System.out.println(x);
		x /= 2; //Equivale a: x = / 2;
		System.out.println(x);
		x %= 3; //Equivale a: x = % 3;
		System.out.println(x);
		
	}
}
